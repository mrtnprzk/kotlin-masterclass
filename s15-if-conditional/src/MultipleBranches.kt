fun main() {
    val number = 27

    if (number <= 10) {
        println("Number is 10 or less")
    } else if (number <= 20) {
        println("Number is greater than 10 but less or equal to 20")
    } else if (number <= 30) {
        println("Number is greater than 20 but less or equal to 30")
    } else {
        println("Number is greater than 30")
    }
}