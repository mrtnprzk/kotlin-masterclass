fun main() {
    println("Hello" + " " + "World!") // Hello World!
    println(18 + 7) // 25
    println(18 - 7) // 11
    println(18 * 7) // 126
    println(18 / 7) // 2
    println(18f / 7f) // 2.5714285
    println(18 % 7) // 4

    var five = 5
    five++
    println(five) // 6

    var ten = 10
    ten--
    println(ten) // 9

    var twenty = 20
    println(twenty++) // 20
    println(twenty) // 21

    var thirty = 30
    println(++thirty) // 31
    println(thirty) // 31
}